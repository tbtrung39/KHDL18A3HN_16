# nhập vào một số tư 1 đến 7 
thu = int(input("nhập vào một số từ 1 đến 7"))
if thu ==1:
    print("thứ 1 là Sunday(chủ nhật)")
elif thu ==2:
    print("thứ 2 là Monday(thứ hai)")
elif thu ==3:
    print("thứ ba là Tuesday(thứ ba)")
elif thu==4:
    print("thứ 4 là Wednesday(thứ tư)")
elif thu ==5:
    print("thứ 5 là Thursday(thứ năm)")
elif thu ==6:
    print("thứ 6 là Friday(thứ sáu)")
elif thu ==7:
    print("thứ 7 là Saturday(thứ bảy)")
else:
    print("số không hợp lệ ! vui lòng nhập lại số")