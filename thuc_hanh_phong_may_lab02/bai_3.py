thu = int(input("Nhập thứ (1->7): "))
if thu == 1:
    print(f"Thứ {thu}: SUNDAY")
elif thu == 2:
    print(f"Thứ {thu}: MONDAY")
elif thu == 3:
    print(f"Thứ {thu}: TUESDAY")
elif thu == 4:
    print(f"Thứ {thu}: WEDNESDAY")
elif thu == 5:
    print(f"Thứ {thu}: THURSDAY")
elif thu == 6:
    print(f"Thứ {thu}: FRIDAY")
elif thu == 7:
    print(f"Thứ {thu}: SATURDAY")
else:
    print("Thứ bạn nhập không hợp lệ, vui lòng nhập lại!")