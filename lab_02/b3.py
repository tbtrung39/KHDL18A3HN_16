
print("nhập các thứ trong tuần:")
thu = int(input("nhập các thứ:"))
if thu == 1:
    print("sunday")
elif thu == 2:
    print("monday")
elif thu == 3:
    print("tuesday")
elif thu == 4:
    print("wednesday")
elif thu == 5:
    print("thursday")
elif thu == 6:
    print("friday")
elif thu == 7:
    print("saturday")
else:
    print("không hợp lệ , xin vui lòng nhập lại")
    