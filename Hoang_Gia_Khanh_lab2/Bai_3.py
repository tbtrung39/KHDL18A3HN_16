ngay = int(input("Nhâp ngày: "))
if ngay == 1:
    print("Sunday")
elif ngay == 2:
    print("Monday")
elif ngay == 3:
    print("Tuesday")
elif ngay == 4:
    print("Wednesday")
elif ngay == 5:
    print("Thursday")
elif ngay == 6:
    print("Friday")
elif ngay == 7:
    print("Saturday")
else:
    print("Nhập lại")